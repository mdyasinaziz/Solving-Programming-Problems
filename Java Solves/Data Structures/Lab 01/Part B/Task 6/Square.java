public class Square {
    public double height = 0;
    public double width = 0;
    public final double area = 12.00;
    public double getHeight(){
        return height;
    }
    
    public double getWidth () {
        return width;
    }
    
    public void setHeight(double h) {
        height = h;
    }
    
    public void setWidth (double w) {
        width = w;
    }
    
    public double getArea () {
    return area;
    }
}
